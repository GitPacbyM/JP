
public class IterationPractice {
//while, do while, for, for each	
	public static void main(String args[])
	{
		//while
		int i = 3; //initial value
		while(i<=5)//condition
		{
			System.out.println("Hi");
			i++;//increment
		}
		//do while - Steps would be executed atleast once even if the condition is false, condition is checked after the steps are executed.
		int n=17;
		
		do {
			System.out.println("Hello");
			n++;
			
		} while(n<15);
		//for loop
		int k = 1;
		for(k=1;k<5;k++)	
		{
			System.out.println("condition is true");
		}

		
		
				
	}

}
